    private void calculateRooms(){
      int h = (new Integer (hc.getValue().toString())).intValue();
      int v = (new Integer (vc.getValue().toString())).intValue();
      Integer rooms = new Integer((h+1)*(v+1));
      r.setText(rooms.toString());
    }

    /**
     * Comprueba la consistencia de los valores introducidos. False si son inconsistentes, por ejemplo la anchura de la escalera es mayor que la anchura del pasillo.
     * @return
     */
    private boolean checkParameters() {
       int corridorw = (new Integer (cw.getValue().toString())).intValue();
       int stairsw= (new Integer (sw.getValue().toString())).intValue();
       if(corridorw < stairsw){
           showError("The corridor width must be bigger or equal than the staris width");
           return false;
       }
       return true;
    }

    
